export function checkRestDay(dateStr: string): { isRest: boolean; label: string } {
  // 공휴일 먼저 확인
  const holidayName = HOLIDAY_NAMES[dateStr]
  if (holidayName) return { isRest: true, label: holidayName }

  // 토·일 확인 (YYYY-MM-DD → 시간대 오류 방지)
  const [y, m, d] = dateStr.split('-').map(Number)
  const day = new Date(y, m - 1, d).getDay()
  if (day === 0) return { isRest: true, label: '일요일' }
  if (day === 6) return { isRest: true, label: '토요일' }

  return { isRest: false, label: '' }
}
